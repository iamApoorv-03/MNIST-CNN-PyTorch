# Defining Cnn Architecture
class SimpleMedicalCNN(nn.Module): # our parent class is nn.Module ,SimpleMedicalCNN is the CHILD, CHILD CLASS INHERITS FROM NN.MODULE

  """
  Simple CNN for medical image classification

 
  Input (28×28×1)
  → Conv2d(1, 32, 3) + ReLU
  → MaxPool2d(2)
  → Conv2d(32, 64, 3) + ReLU
  → MaxPool2d(2)
  → Flatten
  → Linear(1024, 128) + ReLU
  → Linear(128, 10)
  Output (10 classes)
  """
  def __init__(self):
    super(SimpleMedicalCNN, self).__init__()  # super calls parent's__init__ in SimpleMedicalCNN which is child class , SELF is this object

    # Convolutional layers
    self.conv1= nn.Conv2d(
        in_channels=1,  #grayscale image
        out_channels=32, # 32 feature maps
        kernel_size= 3,
        padding= 1       # keep size same

    )

    self.conv2= nn.Conv2d(
        in_channels= 32,
        out_channels= 64,
        kernel_size= 3,
        padding = 1
    )

    #Pooling
    self.pool=nn.MaxPool2d(
        kernel_size=2,
        stride=2
    )

    # fully connected layer
    self.fc1= nn.Linear(64*7*7,128) # after 2 pooling 28->14->7
    self.fc2= nn.Linear(128,10)   # 10 classes(0-9)

    # activation function
    self.relu= nn.ReLU()
    self.dropout = nn.Dropout(0.5)

  def forward(self, x):
    #convolution block 1
    x= self.relu(self.conv1(x)) #(N , 32, 28 , 28) N = no of images in one batch ,32 feature feature map
    x= self.pool(x)#(N ,32, 14, 14)

    # convolution block 2
    x= self.relu(self.conv2(x))# (N , 64, 14, 14)
    x= self.pool(x)# (N , 64, 7 , 7)

    # flatten
    x = x.view(x.size(0),-1) #(N,3136)

    # FC LAYERS
    x = self.relu(self.fc1(x))
    x= self.dropout(x)
    x= self.fc2(x)

    return(x)

model= SimpleMedicalCNN().to(device) # .to(device):If GPU exists: Model on GPU memory(Fast) , If no GPU: Model on CPU memory (Slow)
print(" Model created successfully!")

# count parameters
total_params = sum(p.numel()for p in model.parameters())
