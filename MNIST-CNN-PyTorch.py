# Without num_workers (or num_workers=0):
# ├─ Main process loads 1 batch
# ├─ Wait for loading to finish
# ├─ Train on batch
# ├─ Load next batch (SLOW - waiting time)
# └─ Train on batch
# Result: GPU sits idle while waiting for data

# With num_workers=2:
# ├─ Thread 1: Loading batch N+1 (while training)
# ├─ Main process: Training on batch N
# ├─ Thread 2: Loading batch N+2 (while training)
# └─ No waiting! Data always ready
# Result: GPU always has data, no idle time


# WITHOUT num_workers=0:
# Time: [Load batch] [Train] [Load batch] [Train] [Load batch] [Train]
#       Loading time wasted

# WITH num_workers=2:
# Time: [Load batch] [Load batch] [Train] [Load batch] [Train]
#       Loading happens while training

# Formula:
# normalized_image = (image - mean) / std

# Step by step:
# 1. Subtract mean (0.1307) from all pixels
# 2. Divide by standard deviation (0.3081)

# Result:
# ├─ Values centered around 0
# ├─ Values between roughly -1 and 1
# └─ Easier for neural network to learn!

# training setup
criterion = nn.CrossEntropyLoss()
optimizer= optim.Adam(model.parameters(), lr = 0.0001)
scheduler= optim.lr_scheduler.StepLR(optimizer, step_size= 2, gamma= 0.1)  # Scheduler: StepLR (reduce lr by 10% every 2 epochs)"

# training loop
# single epoch training
def train_epoch(model, train_loader, criterion, optimizer, device):

    model.train()
    total_loss = 0.0
    correct = 0 # how many correct predictions
    total = 0 # total predictions

    for batch_idx, (images, labels) in enumerate(train_loader):
        images, labels = images.to(device), labels.to(device)

        # Forward pass
        outputs = model(images)     # output.shape (32, 10 )->(no of images , 10 probabilities each) (digits: 0-9)
        loss = criterion(outputs, labels)

        # Backward pass
        optimizer.zero_grad()
        loss.backward()
        optimizer.step()

        # Statistics
        total_loss += loss.item()   #loss.item() = (loss_img1 + loss_img2 + ... + loss_img32) / 32  = 2.5  (example number)

        _, predicted = torch.max(outputs.data, 1)    #Underscore = "I don't care about this value", It means: "Store something here but I won't use it"
        total += labels.size(0) # shape of labels (32,)
        correct += (predicted == labels).sum().item()

        if (batch_idx + 1) % 500 == 0:
            print(f"  Batch [{batch_idx+1}/{len(train_loader)}]")

    avg_loss = total_loss / len(train_loader)
    accuracy = 100.0 * correct / total
    return avg_loss, accuracy

def test_model(model, test_loader, criterion, device):

    model.eval()
    total_loss = 0.0
    correct = 0
    total = 0

    with torch.no_grad():
        for images, labels in test_loader:
            images, labels = images.to(device), labels.to(device)
            outputs = model(images)
            loss = criterion(outputs, labels)

            total_loss += loss.item()
            _, predicted = torch.max(outputs.data, 1)
            total += labels.size(0)
            correct += (predicted == labels).sum().item()

    avg_loss = total_loss / len(test_loader)
    accuracy = 100.0 * correct / total
    return avg_loss, accuracy

# Train for epochs
num_epochs = 10
train_losses, train_accuracy = [], []
test_losses, test_accuracy = [], []

start_time = time.time()

for epoch in range(num_epochs):
    print(f"\nEpoch [{epoch+1}/{num_epochs}]")

    train_loss, train_acc = train_epoch(model, train_loader, criterion, optimizer, device)
    test_loss, test_acc = test_model(model, test_loader, criterion, device)

    train_losses.append(train_loss)
    train_accuracy.append(train_acc)
    test_losses.append(test_loss)
    test_accuracy.append(test_acc)

    print(f"Train Loss: {train_loss:.4f} | Train Acc: {train_acc:.2f}%")
    print(f"Test Loss:  {test_loss:.4f} | Test Acc:  {test_acc:.2f}%")

    scheduler.step()

elapsed_time = time.time() - start_time
print(f" Training complete! (Time: {elapsed_time:.1f} seconds)")
print(f"Final test accuracy: {test_accuracy[-1]:.2f}%")

